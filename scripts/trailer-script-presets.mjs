function nearestDuration(seconds) {
  if (seconds <= 45) return 30;
  if (seconds <= 75) return 60;
  return 90;
}

export function listTrailerScriptPresets() {
  return Object.entries(TRAILER_PRESETS).map(([id, preset]) => ({
    id,
    title: preset.title,
    style: preset.style,
    sceneType: preset.sceneType,
    durations: Object.keys(preset.byDuration).map(Number),
  }));
}


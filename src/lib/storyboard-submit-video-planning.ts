export function generateTimingDescriptions(count: number): string[] {
  if (count <= 2) {
    return [
      '，开场建立，镜头定焦主体',
      '，收尾定格，最终画面稳定清晰',
    ];
  }

  if (count <= 4) {
    return [
      '，开场建立，镜头定焦主体',
      '，中段推进，动作展开镜头缓慢移动',
      '，细节特写，聚焦主体关键细节',
      '，收尾定格，最终画面稳定清晰',
    ];
  }

  return [
    '，初始状态，镜头定焦主体',
    '，主体开始动作',
    '，动作逐渐展开，镜头缓慢移动',
    '，动作进入高潮',
    '，关键时刻，镜头保持稳定',
    '，动作开始收尾',
    '，场景转换或视角变化',
    '，接近尾声',
    '，最终画面，定格或缓缓淡出',
  ];
}
